package fx.dom.html;
import org.w3c.dom.html.HTMLLinkElement;
class HtmlLinkElement extends HtmlElement implements HTMLLinkElement {

  @Override public boolean getDisabled() { return false; }
  @Override public void setDisabled(boolean disabled) {}
  @Override public String getCharset() { return null; }
  @Override public void setCharset(String charset) {}
  @Override public String getHref() { return null; }
  @Override public void setHref(String href) {}
  @Override public String getHreflang() { return null; }
  @Override public void setHreflang(String hreflang) {}
  @Override public String getMedia() { return null; }
  @Override public void setMedia(String media) {}
  @Override public String getRel() { return null; }
  @Override public void setRel(String rel) {}
  @Override public String getRev() { return null; }
  @Override public void setRev(String rev) {}
  @Override public String getTarget() { return null; }
  @Override public void setTarget(String target) {}
  @Override public String getType() { return null; }
  @Override public void setType(String type) {}
}
