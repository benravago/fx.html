package fx.dom.html;
import org.w3c.dom.html.HTMLAreaElement;
class HtmlAreaElement extends HtmlElement implements HTMLAreaElement {

  @Override public String getAccessKey() { return null; }
  @Override public void setAccessKey(String accessKey) {}
  @Override public String getAlt() { return null; }
  @Override public void setAlt(String alt) {}
  @Override public String getCoords() { return null; }
  @Override public void setCoords(String coords) {}
  @Override public String getHref() { return null; }
  @Override public void setHref(String href) {}
  @Override public boolean getNoHref() { return false; }
  @Override public void setNoHref(boolean noHref) {}
  @Override public String getShape() { return null; }
  @Override public void setShape(String shape) {}
  @Override public int getTabIndex() { return 0; }
  @Override public void setTabIndex(int tabIndex) {}
  @Override public String getTarget() { return null; }
  @Override public void setTarget(String target) {}
}
